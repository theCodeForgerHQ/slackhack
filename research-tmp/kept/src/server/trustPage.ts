const BUCKET_META: Record<TrustBucket, { label: string; accent: string; tint: string }> = {
  kept: { label: "Kept", accent: TEAL, tint: "rgba(15,110,92,0.10)" },
  in_progress: { label: "In progress", accent: "#8A6D1F", tint: "rgba(138,109,31,0.10)" },
  verifying: { label: "Verifying", accent: "#3A5A9B", tint: "rgba(58,90,155,0.10)" },
  at_risk: { label: "At risk", accent: "#A23B2E", tint: "rgba(162,59,46,0.10)" },
};

const MONTHS = ["Jan", "Feb", "Mar", "Apr", "May", "Jun", "Jul", "Aug", "Sep", "Oct", "Nov", "Dec"];

function esc(s: string): string {
  return s.replace(/[&<>"']/g, (c) => ({ "&": "&amp;", "<": "&lt;", ">": "&gt;", '"': "&quot;", "'": "&#39;" })[c]!);
}

/**
 * Fail-closed boundary (invariant #5): re-scan any free-text before it is emitted, with
 * the SAME detector the D1 sanitizer uses. `buildTrustView` already genericized leaky
 * labels; this is defense-in-depth so a bug upstream can never surface a ref/internal token.
 */
function safeText(s: string): string {
  return detectLeaks(s).length === 0 ? esc(s) : "Commitment";
}

/** Format a YYYY-MM-DD or ISO string as "Jun 30, 2026" (UTC, deterministic). */
function fmtDate(input: string | null): string | null {
  if (!input) return null;
  const d = new Date(input.length <= 10 ? `${input}T00:00:00Z` : input);
  if (Number.isNaN(d.getTime())) return null;
  return `${MONTHS[d.getUTCMonth()]} ${d.getUTCDate()}, ${d.getUTCFullYear()}`;
}

function keptMark(): string {
  // Inline the Kept "K" logo mark (no external asset; CSP-safe).
  return `<span class="mark">K</span>`;
}

function renderItem(item: TrustItem): string {
  const meta = BUCKET_META[item.bucket];
  const due = fmtDate(item.due);
  const keptOn = fmtDate(item.keptOn);
  const sub =
    item.bucket === "kept" && keptOn
      ? `Kept on ${esc(keptOn)}`
      : due
        ? `Due ${esc(due)}`
        : "In our ledger";
  return `
      <li class="row">
        <span class="pill" style="color:${meta.accent};background:${meta.tint};">${esc(meta.label)}</span>
        <span class="body">
          <span class="label">${safeText(item.label)}</span>
          <span class="sub">${sub}</span>
        </span>
      </li>`;
}

function renderCounts(view: TrustView): string {
  return TRUST_BUCKETS.map((b) => {
    const meta = BUCKET_META[b];
    return `
        <div class="stat">
          <div class="stat-n" style="color:${meta.accent};">${view.counts[b]}</div>
          <div class="stat-l">${esc(meta.label)}</div>
        </div>`;
  }).join("");
}

