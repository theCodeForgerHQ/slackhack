/**
 * W6 — the customer trust page (HTTP surface). `GET /trust/:token` resolves the opaque
 * capability to an audience-safe `TrustView` (built in `src/app/trustView.ts`, gated by
 * the SAME D1 policy as the customer channel) and renders a small, self-contained,
 * branded HTML page matching the Vercel design tokens (paper / teal, Fraunces / IBM Plex).
 *
 * Response discipline: `X-Robots-Tag: noindex` + `Cache-Control: private, no-store` on
 * every response; a per-IP rate limit; and an identical 404 for unknown AND revoked
 * tokens (no existence leak). A "generated by Kept" footer signs the page.
 */

