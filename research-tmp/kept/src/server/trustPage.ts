function footer(view?: TrustView): string {
  const withheld =
    view && view.redactedInternalCount > 0
      ? `${view.redactedInternalCount} internal detail${view.redactedInternalCount === 1 ? "" : "s"} withheld`
      : "Internal details are never shown here";
  const stamp = view ? `Generated ${esc(fmtDate(view.generatedAt) ?? "")}` : "";
  return `
    <footer>
      <span class="gen"><span class="dot">K</span> Generated by Kept</span>
      <span>${esc(withheld)}${stamp ? ` · ${stamp}` : ""}</span>
    </footer>`;
}

/** Render the audience-safe trust page for one customer. */
export function renderTrustPage(view: TrustView): string {
  const bySection = TRUST_BUCKETS.map((bucket) => {
    const rows = view.items.filter((it) => it.bucket === bucket);
    if (rows.length === 0) return "";
    return `
      <section>
        <h2>${esc(BUCKET_META[bucket].label)} · ${rows.length}</h2>
        <ul>${rows.map(renderItem).join("")}</ul>
      </section>`;
  }).join("");

  const body = `
    <header>${keptMark()}<span class="brand">Kept</span></header>
    <h1>Commitments to ${safeText(view.customer)}</h1>
    <p class="lede">A live, human-verified view of what we've promised you and where each promise stands.</p>
    <div class="stats">${renderCounts(view)}</div>
    ${view.items.length === 0 ? `<div class="empty">No open commitments on record right now.</div>` : bySection}
    ${footer(view)}`;

  return shell(`Commitments to ${view.customer} · Kept`, body);
}

/** The 404 page — identical for unknown AND revoked tokens (no existence leak). */
export function renderTrustNotFound(): string {
  const body = `
    <header>${keptMark()}<span class="brand">Kept</span></header>
    <h1>This link isn't available</h1>
    <p class="lede">The link may have expired or been revoked. Please ask your account team for a fresh one.</p>
    ${footer()}`;
  return shell("Not available · Kept", body);
}

/**
 * A simple in-memory fixed-window rate limiter (best-effort, per instance). Enough to
 * blunt scraping/abuse of the public route; not a distributed limiter.
 */
export class TrustRateLimiter {
  private readonly hits = new Map<string, { count: number; resetAt: number }>();
  constructor(
    private readonly limit = 60,
    private readonly windowMs = 60_000,
  ) {}

  allow(key: string, now: number = Date.now()): boolean {
    const e = this.hits.get(key);
    if (!e || now >= e.resetAt) {
      if (this.hits.size > 5000) this.prune(now); // bound memory across many IPs
      this.hits.set(key, { count: 1, resetAt: now + this.windowMs });
      return true;
    }
    if (e.count >= this.limit) return false;
    e.count++;
    return true;
  }

  private prune(now: number): void {
    for (const [k, v] of this.hits) if (now >= v.resetAt) this.hits.delete(k);
  }
}

function clientIp(req: IncomingMessage): string {
  const fwd = req.headers["x-forwarded-for"];
  const first = Array.isArray(fwd) ? fwd[0] : fwd?.split(",")[0];
  return (first ?? req.socket?.remoteAddress ?? "unknown").trim();
}

/** Fallback token extraction if the router didn't populate req.params (standalone server). */
function tokenFromPath(url: string | undefined): string | undefined {
  const path = new URL(url ?? "/", "http://localhost").pathname;
  const m = /^\/trust\/([^/]+)\/?$/.exec(path);
  return m ? decodeURIComponent(m[1]) : undefined;
}

function sendHtml(res: ServerResponse, status: number, html: string): void {
  res.statusCode = status;
  res.setHeader("content-type", "text/html; charset=utf-8");
  // A private, per-recipient capability: keep it out of search indexes and shared caches.
  res.setHeader("X-Robots-Tag", "noindex");
  res.setHeader("Cache-Control", "private, no-store");
  res.setHeader("Referrer-Policy", "no-referrer");
  res.end(html);
}

/**
 * The `GET /trust/:token` request handler. Rate-limit → resolve the token to a scoped,
 * audience-safe view → render. Unknown/revoked/missing tokens (and rate-limited requests)
 * never reveal whether a token exists. Exported for tests.
 */
export async function handleTrustRequest(
  getOrch: () => KeptOrchestrator,
  limiter: TrustRateLimiter,
  req: IncomingMessage & { params?: Record<string, string | string[] | undefined> },
  res: ServerResponse,
): Promise<void> {
  if (!limiter.allow(clientIp(req))) {
    res.statusCode = 429;
    res.setHeader("content-type", "text/plain; charset=utf-8");
    res.setHeader("X-Robots-Tag", "noindex");
    res.setHeader("Cache-Control", "private, no-store");
    res.setHeader("Retry-After", "60");
    res.end("Too many requests");
    return;
  }
  const raw = req.params?.token;
  const token = (Array.isArray(raw) ? raw[0] : raw) ?? tokenFromPath(req.url);
  const view = token ? await getOrch().trustPageForToken(token) : null;
  if (!view) {
    sendHtml(res, 404, renderTrustNotFound());
    return;
  }
  sendHtml(res, 200, renderTrustPage(view));
}

/** Build the Bolt customRoute for the trust page (used by keptCustomRoutes). */
export function trustCustomRoute(getOrch: () => KeptOrchestrator, limiter: TrustRateLimiter = new TrustRateLimiter()): CustomRoute {
  return {
    path: "/trust/:token",
    method: "GET",
    handler: (req, res) => {
      handleTrustRequest(getOrch, limiter, req, res).catch((err) => {
        res.statusCode = 500;
        res.end(String(err instanceof Error ? err.message : err));
      });
    },
  };
}
